// MARK: - VideoMetadata

@Suite("VideoMetadata")
struct VideoMetadataTests {

    @Test("url is constructed from videoId")
    func url() {
        let meta = VideoMetadata(
            videoId: "abc123def45",
            title: "T", author: "A",
            publishDate: nil, duration: nil, viewCount: nil,
            description: nil, keywords: []
        )
        #expect(meta.url == "https://www.youtube.com/watch?v=abc123def45")
    }

    @Test("year extracts from publishDate")
    func yearFromDate() {
        let meta = VideoMetadata(
            videoId: "x", title: "T", author: "A",
            publishDate: "2023-07-04", duration: nil, viewCount: nil,
            description: nil, keywords: []
        )
        #expect(meta.year == "2023")
    }

    @Test("year falls back to current year when no publishDate")
    func yearFallback() {
        let meta = VideoMetadata(
            videoId: "x", title: "T", author: "A",
            publishDate: nil, duration: nil, viewCount: nil,
            description: nil, keywords: []
        )
        let formatter = DateFormatter()
        formatter.dateFormat = "yyyy"
        #expect(meta.year == formatter.string(from: Date()))
    }

    @Test("channelURL removes spaces from author")
    func channelURL() {
        let meta = VideoMetadata(
            videoId: "x", title: "T", author: "Cool Channel",
            publishDate: nil, duration: nil, viewCount: nil,
            description: nil, keywords: []
        )
        #expect(meta.channelURL == "https://www.youtube.com/@CoolChannel")
    }
}

// MARK: - CaptionTrack

@Suite("CaptionTrack")
struct CaptionTrackTests {

    @Test("isAutoGenerated when kind is asr")
    func autoGenerated() {
        let track = CaptionTrack(baseUrl: "", languageCode: "en", kind: "asr", name: nil)
        #expect(track.isAutoGenerated)
    }

    @Test("not auto-generated when kind is nil")
    func manualTrack() {
        let track = CaptionTrack(baseUrl: "", languageCode: "en", kind: nil, name: nil)
        #expect(!track.isAutoGenerated)
    }

    @Test("displayName from simpleText")
    func displayNameSimple() {
        let track = CaptionTrack(
            baseUrl: "", languageCode: "en", kind: nil,
            name: TrackName(simpleText: "English", runs: nil)
        )
        #expect(track.displayName == "English")
    }

    @Test("displayName from runs")
    func displayNameRuns() {
        let track = CaptionTrack(
            baseUrl: "", languageCode: "en", kind: nil,
            name: TrackName(simpleText: nil, runs: [
                .init(text: "English"),
                .init(text: " (auto)"),
            ])
        )
        #expect(track.displayName == "English (auto)")
    }

    @Test("displayName falls back to languageCode")
    func displayNameFallback() {
        let track = CaptionTrack(baseUrl: "", languageCode: "fr", kind: nil, name: nil)
        #expect(track.displayName == "fr")
    }
}

