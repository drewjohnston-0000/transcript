// MARK: - JSON3Response.Event

@Suite("JSON3Response.Event")
struct EventTests {

    @Test("text joins segments")
    func textJoinsSegs() {
        let event = JSON3Response.Event(
            tStartMs: 0, dDurationMs: 1000,
            segs: [.init(utf8: "Hello "), .init(utf8: "world")]
        )
        #expect(event.text == "Hello world")
    }

    @Test("text is empty when no segments")
    func textEmptyNoSegs() {
        let event = JSON3Response.Event(tStartMs: 0, dDurationMs: 1000, segs: nil)
        #expect(event.text == "")
    }

    @Test("text skips nil segment values")
    func textSkipsNilSegs() {
        let event = JSON3Response.Event(
            tStartMs: 0, dDurationMs: 1000,
            segs: [.init(utf8: "Hello"), .init(utf8: nil), .init(utf8: " world")]
        )
        #expect(event.text == "Hello world")
    }
}

// MARK: - JSON Decoding

@Suite("Model Decoding")
struct ModelDecodingTests {

    @Test("decodes InnerTubeResponse with captions")
    func decodeResponse() throws {
        let json = """
        {
            "captions": {
                "playerCaptionsTracklistRenderer": {
                    "captionTracks": [
                        {
                            "baseUrl": "https://example.com/caps",
                            "languageCode": "en",
                            "kind": "asr",
                            "name": { "simpleText": "English (auto-generated)" }
                        }
                    ]
                }
            },
            "videoDetails": {
                "videoId": "abc123",
                "title": "Test",
                "author": "Author",
                "lengthSeconds": "300",
                "viewCount": "5000"
            }
        }
        """
        let response = try JSONDecoder().decode(InnerTubeResponse.self, from: json.data(using: .utf8)!)
        let tracks = response.captions?.playerCaptionsTracklistRenderer?.captionTracks
        #expect(tracks?.count == 1)
        #expect(tracks?[0].languageCode == "en")
        #expect(tracks?[0].isAutoGenerated == true)
        #expect(response.videoDetails?.title == "Test")
        #expect(response.videoDetails?.viewCount == "5000")
    }

    @Test("decodes JSON3Response")
    func decodeJSON3() throws {
        let json = """
        {
            "events": [
                {
                    "tStartMs": 500,
                    "dDurationMs": 2000,
                    "segs": [{"utf8": "Hello "}, {"utf8": "world"}]
                }
            ]
        }
        """
        let response = try JSONDecoder().decode(JSON3Response.self, from: json.data(using: .utf8)!)
        #expect(response.events?.count == 1)
        #expect(response.events?[0].text == "Hello world")
        #expect(response.events?[0].tStartMs == 500)
    }
}
